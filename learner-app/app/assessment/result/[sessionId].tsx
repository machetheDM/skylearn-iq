import React, { useEffect, useState } from "react";
import {
  View, Text, ScrollView, TouchableOpacity,
  StyleSheet, ActivityIndicator,
} from "react-native";
import { useLocalSearchParams, useRouter } from "expo-router";
import { Ionicons } from "@expo/vector-icons";
import { useAuth } from "../../../context/AuthContext";
import { API_URL } from "../../../constants/api";
import { C } from "../../../constants/colors";

type Answer = {
  id: number; question_id: number; is_correct: boolean | null;
  marks_awarded: number; text_answer: string | null;
  selected_option_id: number | null; auto_marked: boolean;
};
type Score = {
  total_marks_awarded: number; total_marks_possible: number;
  percentage: number; pass_fail: boolean | null;
};
type Feedback = {
  id: number; generated_by: string; content: string;
  strength_areas: string | null; weakness_areas: string | null;
  recommendations: string | null;
};
type Session = {
  id: number; assessment_id: number; status: string;
  started_at: string; completed_at: string | null;
  answers: Answer[]; score: Score | null; feedbacks: Feedback[];
};

export default function ResultScreen() {
  const { sessionId } = useLocalSearchParams<{ sessionId: string }>();
  const { token }     = useAuth();
  const router        = useRouter();
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    (async () => {
      try {
        const res = await fetch(`${API_URL}/api/sessions/${sessionId}`, {
          headers: { Authorization: `Bearer ${token}` },
        });
        if (res.ok) setSession(await res.json());
      } catch {} finally { setLoading(false); }
    })();
  }, [sessionId]);

  if (loading) return <ActivityIndicator style={{ flex: 1 }} color={C.primary} />;
  if (!session?.score) return (
    <View style={s.container}>
      <Text style={{ color: C.textMuted, textAlign: "center", marginTop: 60 }}>Result not available yet.</Text>
    </View>
  );

  const { score } = session;
  const pct   = Math.round(score.percentage);
  const pass  = score.pass_fail;
  const color = pass === true ? C.accent : C.danger;
  const correct   = session.answers.filter(a => a.is_correct === true).length;
  const incorrect = session.answers.filter(a => a.is_correct === false).length;
  const pending   = session.answers.filter(a => a.is_correct === null).length;

  return (
    <ScrollView style={s.container} contentContainerStyle={{ paddingBottom: 40 }}>
      {/* Hero score */}
      <View style={[s.hero, { backgroundColor: color }]}>
        <Text style={s.pct}>{pct}%</Text>
        <Text style={s.verdict}>{pass === true ? "🎉 PASSED!" : pass === false ? "❌ Not Yet" : "⏳ Pending"}</Text>
        <Text style={s.marksText}>
          {score.total_marks_awarded}/{score.total_marks_possible} marks
        </Text>
      </View>

      {/* Breakdown */}
      <View style={s.breakdown}>
        <BreakItem icon="checkmark-circle" color={C.accent} label="Correct"  value={correct} />
        <BreakItem icon="close-circle"    color={C.danger}  label="Wrong"    value={incorrect} />
        <BreakItem icon="help-circle"     color={C.warning} label="Pending"  value={pending} />
      </View>

      {/* AI Feedback */}
      {session.feedbacks.length > 0 && session.feedbacks.map(fb => (
        <View key={fb.id} style={s.feedbackCard}>
          <View style={s.fbHeader}>
            <Ionicons name="sparkles" size={18} color={C.primary} />
            <Text style={s.fbTitle}>🧠 AI Feedback</Text>
          </View>
          <Text style={s.fbContent}>{fb.content}</Text>

          {fb.strength_areas && (
            <View style={[s.fbSection, { borderLeftColor: C.accent }]}>
              <Text style={[s.fbSectionTitle, { color: C.accent }]}>✅ Strengths</Text>
              <Text style={s.fbSectionText}>{fb.strength_areas}</Text>
            </View>
          )}
          {fb.weakness_areas && (
            <View style={[s.fbSection, { borderLeftColor: C.danger }]}>
              <Text style={[s.fbSectionTitle, { color: C.danger }]}>⚠️ Needs Work</Text>
              <Text style={s.fbSectionText}>{fb.weakness_areas}</Text>
            </View>
          )}
          {fb.recommendations && (
            <View style={[s.fbSection, { borderLeftColor: C.primary }]}>
              <Text style={[s.fbSectionTitle, { color: C.primary }]}>💡 Recommendations</Text>
              <Text style={s.fbSectionText}>{fb.recommendations}</Text>
            </View>
          )}
        </View>
      ))}

      {session.feedbacks.length === 0 && (
        <View style={s.noFeedback}>
          <Ionicons name="hourglass-outline" size={32} color={C.textMuted} />
          <Text style={s.noFbText}>AI feedback will appear here once generated by your tutor.</Text>
        </View>
      )}

      {/* Actions */}
      <TouchableOpacity style={s.homeBtn} onPress={() => router.replace("/(tabs)")}>
        <Ionicons name="home-outline" size={18} color={C.white} />
        <Text style={s.homeBtnText}>Back to Home</Text>
      </TouchableOpacity>
      <TouchableOpacity style={s.moreBtn} onPress={() => router.push("/(tabs)/assessments")}>
        <Text style={s.moreBtnText}>Take Another Assessment</Text>
      </TouchableOpacity>
    </ScrollView>
  );
}

function BreakItem({ icon, color, label, value }: { icon: any; color: string; label: string; value: number }) {
  return (
    <View style={br.item}>
      <Ionicons name={icon} size={22} color={color} />
      <Text style={br.val}>{value}</Text>
      <Text style={br.lbl}>{label}</Text>
    </View>
  );
}

const s = StyleSheet.create({
  container:   { flex: 1, backgroundColor: C.bg },
  hero:        { alignItems: "center", paddingVertical: 48, paddingTop: 60 },
  pct:         { fontSize: 64, fontWeight: "900", color: C.white },
  verdict:     { fontSize: 20, fontWeight: "700", color: C.white, marginTop: 4 },
  marksText:   { fontSize: 16, color: "rgba(255,255,255,0.8)", marginTop: 8 },
  breakdown:   { flexDirection: "row", backgroundColor: C.card, marginHorizontal: 16, marginTop: -20, borderRadius: 16, padding: 16, elevation: 6, shadowColor: "#000", shadowOpacity: 0.1, shadowRadius: 10, borderWidth: 1, borderColor: C.border },
  feedbackCard:{ margin: 16, backgroundColor: C.card, borderRadius: 16, padding: 16, borderWidth: 1, borderColor: C.border },
  fbHeader:    { flexDirection: "row", alignItems: "center", gap: 8, marginBottom: 12 },
  fbTitle:     { fontWeight: "700", fontSize: 16, color: C.textPrimary },
  fbContent:   { fontSize: 14, color: C.textSecondary, lineHeight: 22, marginBottom: 14 },
  fbSection:   { borderLeftWidth: 3, paddingLeft: 12, marginBottom: 10 },
  fbSectionTitle: { fontWeight: "700", fontSize: 13, marginBottom: 4 },
  fbSectionText:  { fontSize: 13, color: C.textSecondary, lineHeight: 20 },
  noFeedback:  { alignItems: "center", margin: 24, gap: 10 },
  noFbText:    { color: C.textMuted, fontSize: 13, textAlign: "center", lineHeight: 20 },
  homeBtn:     { flexDirection: "row", alignItems: "center", justifyContent: "center", gap: 8, backgroundColor: C.primary, margin: 16, borderRadius: 12, paddingVertical: 14 },
  homeBtnText: { color: C.white, fontWeight: "700", fontSize: 15 },
  moreBtn:     { alignItems: "center", marginHorizontal: 16, marginBottom: 8, paddingVertical: 14, borderRadius: 12, borderWidth: 1.5, borderColor: C.primary },
  moreBtnText: { color: C.primary, fontWeight: "700", fontSize: 15 },
});
const br = StyleSheet.create({
  item: { flex: 1, alignItems: "center", gap: 4 },
  val:  { fontSize: 22, fontWeight: "800", color: C.textPrimary },
  lbl:  { fontSize: 11, color: C.textMuted },
});
